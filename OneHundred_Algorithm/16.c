// 输入两个正整数m和n,求其最大公约数和最小公倍数
// 最大公约数用辗转相除法(欧几里得算法)
/* 
   欧几里得算法:
   用较大数除以较小数，再用出现的余数（第一余数）去除除数
   再用出现的余数（第二余数）去除第一余数
   如此反复，直到最后余数是0为止,
   那么这个除数就是最大到公约数 */
   
// 最小公倍数;两数相乘除以他们的最大公约数
#include<stdio.h>
int main(void)
{
	int n, m;
	int a, b, c;
	printf("输入两个要求的数(空格隔开):");
	scanf("%d %d",&n,&m);
	if (n < m)
	{
		a = n;
		n = m;
		m = a;
	}
	b = n * m;
	c = n % m;
	while (c != 0)
	{
		n=m;
		m=c;
		c=n%m;
		
	}
	printf("最大公约数是:%d 最小公倍数是:%d", m, b / m);
	return 0;
}
